def generate_bot_response(text: str) -> str:
    t = text.lower()
    if "привет" in t or "здрав" in t:
        return "Здравствуйте! Чем могу помочь?"
    elif "пароль" in t:
        return "Вы можете сбросить пароль через форму восстановления."
    elif "ошиб" in t or "не работает" in t:
        return "Пожалуйста, опишите подробнее, что не работает."
    elif "спасибо" in t:
        return "Всегда пожалуйста! Обращайтесь ещё."
    else:
        return "Извините, я не совсем понял. Могу помочь с паролем, ошибками или регистрацией."